	private void lp_file_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lp_file_buttonActionPerformed
		// TODO add your handling code here:
            //Select a lp file
            File SelectedFile;
            //File chooser for selecting the lp file.
            JFileChooser jfc = new JFileChooser("Select LP file");
            jfc.setFileSelectionMode(JFileChooser.FILES_ONLY);
            int result = jfc.showOpenDialog(new JFrame());
            if (result == JFileChooser.APPROVE_OPTION) {
                if (jfc.getSelectedFile().isFile()) {
                    SelectedFile = jfc.getSelectedFile();
                    lp_file_tb.setText(SelectedFile.getAbsolutePath());
                } else {
                    System.err.println("Is not a file.");
                }
            }

	}//GEN-LAST:event_lp_file_buttonActionPerformed

	private void browse_folder_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browse_folder_buttonActionPerformed
		    //Select the project folder
            File SelectedFile;
            //File chooser for selecting the folder
            JFileChooser jfc = new JFileChooser("Select the project folder");
            jfc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
            int result = jfc.showOpenDialog(new JFrame());
            if (result == JFileChooser.APPROVE_OPTION) {
                if (jfc.getSelectedFile().isDirectory()) {
                    SelectedFile = jfc.getSelectedFile();
                    project_folder_tb.setText(SelectedFile.getAbsolutePath());
                } else {
                    SelectedFile = jfc.getSelectedFile().getParentFile();
                    project_folder_tb.setText(SelectedFile.getAbsolutePath());
                    System.err.println("Is not a directory.");
                }
            }
	}//GEN-LAST:event_browse_folder_buttonActionPerformed

