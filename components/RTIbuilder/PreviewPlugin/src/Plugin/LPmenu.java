	private void ok_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ok_buttonActionPerformed
		// TODO add your handling code here:
		boolean error  =  false;
		File lp = new File(lp_file_tb.getText());
		if(!lp.exists()){
			error =  true;
			lp_file_warning.setText("File not found!");
		}
		else{
		    lp_file_warning.setText("");
			lp_file =  lp;
		}
		File folder = new File(project_folder_tb.getText());
		if(!folder.exists()){
			error =  true;
			project_folder_warning.setText("Folder not found!");
		}
		else{
			project_folder_warning.setText("");
			project_folder = folder;
		}

		returned = !error;
		if(!error){
			setVisible(false);
		}
	}//GEN-LAST:event_ok_buttonActionPerformed

